# Day 001: Python Basics - Variables and Data Types

# Integer
age = 25
print(f"Age: {age}, Type: {type(age)}")

# Float
height = 5.9
print(f"Height: {height}, Type: {type(height)}")

# String
name = "Code Learner"
print(f"Name: {name}, Type: {type(name)}")

# Boolean
is_learning = True
print(f"Is Learning: {is_learning}, Type: {type(is_learning)}")

# Type Conversion
age_str = str(age)
height_int = int(height)
print(f"\nConverted age to string: '{age_str}'")
print(f"Converted height to int: {height_int}")

# Multiple assignment
x, y, z = 10, 20, 30
print(f"\nMultiple assignment: x={x}, y={y}, z={z}")

# Arithmetic operations
sum_result = x + y
product = x * y
division = y / x
print(f"\nSum: {sum_result}, Product: {product}, Division: {division}")
